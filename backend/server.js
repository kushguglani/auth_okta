const startServer = async () => {
  try {
    console.log('ğŸ”§ Starting server initialization...\n');

    // Step 1: Connect to MongoDB
    await connectDB();

    // Step 2: Connect to Redis (or use in-memory fallback)
    await connectRedis();

    // Step 3: Start Apollo GraphQL Server
    await startApolloServer(app, PORT);

    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    //  ERROR HANDLING (MUST BE AFTER APOLLO SERVER)
    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•

    // 404 Handler - Must be after all routes
    app.use((req, res) => {
      res.status(404).json({
        error: 'Not Found',
        message: `Cannot ${req.method} ${req.path}`,
        suggestion: 'Check the API documentation for available endpoints'
      });
    });

    // Global Error Handler
    app.use((err, req, res, next) => {
      console.error('Error:', err);
      res.status(err.status || 500).json({
        error: 'Internal Server Error',
        message: process.env.NODE_ENV === 'production'
          ? 'Something went wrong'
          : err.message
      });
    });

    // Step 4: Start Express HTTP Server
    app.listen(PORT, () => {
      console.log('\n' + '='.repeat(50));
      console.log(`ğŸš€ Server running on port ${PORT}`);
      console.log(`ğŸ“¡ REST API: http://localhost:${PORT}/api`);
      console.log(`ğŸ® GraphQL: http://localhost:${PORT}/graphql`);
      console.log(`ğŸŒ Environment: ${process.env.NODE_ENV || 'development'}`);
      console.log('='.repeat(50) + '\n');
    });
  } catch (error) {
    console.error('âŒ Failed to start server:', error);
    process.exit(1);
  }
};

